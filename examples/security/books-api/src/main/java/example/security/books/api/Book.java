package example.security.books.api;

/**
 * @author Sergio del Amo
 * @since 1.0
 */
public class Book {
    String slug;
    String title;
    String author;
    String href;
    String about;
    String image;

    public Book(String slug, String title, String author, String href, String about, String image) {
        this.slug = slug;
        this.title = title;
        this.author = author;
        this.href = href;
        this.about = about;
        this.image = image;
    }

    public String getSlug() {
        return slug;
    }

    public void setSlug(String slug) {
        this.slug = slug;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    public String getHref() {
        return href;
    }

    public void setHref(String href) {
        this.href = href;
    }

    public String getAbout() {
        return about;
    }

    public void setAbout(String about) {
        this.about = about;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
}
